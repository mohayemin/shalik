package org.shalik.test.testdoubles;

import java.util.Date;

public class AllTypes {
	private short shortInteger;
	private int interger;
	private long longInteger;
	private float singleFloat;
	private double doubleFloat;
	private Date date;
	private char character;
	private boolean bool;
	private String string;
	
	public short getShortInteger() {
		return shortInteger;
	}

	public void setShortInteger(short shortInteger) {
		this.shortInteger = shortInteger;
	}

	public int getInterger() {
		return interger;
	}

	public void setInterger(int interger) {
		this.interger = interger;
	}

	public long getLongInteger() {
		return longInteger;
	}

	public void setLongInteger(long longInteger) {
		this.longInteger = longInteger;
	}

	public float getSingleFloat() {
		return singleFloat;
	}

	public void setSingleFloat(float singleFloat) {
		this.singleFloat = singleFloat;
	}

	public double getDoubleFloat() {
		return doubleFloat;
	}

	public void setDoubleFloat(double doubleFloat) {
		this.doubleFloat = doubleFloat;
	}

	public Date getDate() {
		return date;
	}

	public void setDate(Date date) {
		this.date = date;
	}

	public char getCharacter() {
		return character;
	}

	public void setCharacter(char character) {
		this.character = character;
	}

	public boolean isBool() {
		return bool;
	}

	public void setBool(boolean bool) {
		this.bool = bool;
	}

	public String getString() {
		return string;
	}

	public void setString(String string) {
		this.string = string;
	}
}
